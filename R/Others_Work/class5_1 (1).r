solve.equation <- function (a, b, c) {
       d <- (b^2) - (4*a*c) 
       if (d > 0) {
       x1 <- (-b+sqrt(d))/(2*a)
       x2 <- (-b-sqrt(d))/(2*a)
       x <- c(x1, x2)
      } else if (d == 0) {
        x <- (-b)/(2*a)
      } else {
        x <- NULL
      }
       return(x)
       }

